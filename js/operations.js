/* Operaciones de Comparacion */
/* 

=== identico a (valor y tipo de dato)
== igual a
!== distinto a (valor y tipo de dato)
!= diferente 
> mayor que
< menor que
>= mayor igual que
<= menot igual que

*/

5 === '5' // false
5 == '5' //  true

5 !== '5' // true
5 != '5' // false

