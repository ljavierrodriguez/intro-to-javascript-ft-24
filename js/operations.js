/* Operadores Logicas */
/* 
&& (and)
|| (or)
!  (not)
*/
/* 
(AND)
true && true = true
true && false = false
false && false = false

*/

(10 < 5) && (10 < 11)

/* 
(OR)
true || true = true
true || false = true
false || false = false
*/

10 < 5 || 10 < 8 || 10 < 15

/* 
NOT (AND)
!true && !true = false
!true && !false = false
!false && !false = true
*/
(10 < 5) && (10 < 11)

/*
NOT (OR)
!true || !true = false
!true || !false = true
!false || !false = true
*/

!(10 < 5) || !(10 < 8) || !(10 < 15)